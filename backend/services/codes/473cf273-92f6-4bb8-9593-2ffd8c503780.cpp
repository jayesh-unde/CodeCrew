#include <iostream>
using namespace std;

int main() {
    int a = 5;
    int b = 3;
    cout << "Addition: " << a + b << endl;
    cout << "Subtraction: " << a - b << endl;
    cout << "Multiplication: " << a * b << endl;
    cout << "Division: " << a / b << endl;  // Integer division
    return 0;
}
